// Да се напише програма, която изписва в конзолата сумата на четните числа, които се делят на 3, в интервала [1 - 20] включително.

function oddNumbers () {
    let sum = 0;
for(let i=1; i<=20; i++) {
    if(i % 2 === 0 && i % 3 === 0) {
       sum += i;
    }
};
console.log(sum)
}
oddNumbers();




// очакван отговор: sum = 3